value1 = int(input())
symbol = str(input())
value2 = int(input())
if symbol == "+":
    print(value1 + value2)
elif symbol == "-":
    print(value1 - value2)
elif symbol == "*":
    print(value1 * value2)
elif symbol == "/":
    print(value1 / value2)
elif symbol == "**":
    print(value1 ** value2)
